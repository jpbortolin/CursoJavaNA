package ExerciciosDeAprendizado;

public class Exemplo2 {

	public static void main(String[] args) {
		
		//Processamento de Dados / Casting:
		
		//Multiplicações:
		int x;
		double y;
		
			x = 5;
			y = 2 * x;
			
			System.out.println(x);
			System.out.println(y);
			
			System.out.println("");
			
		//Divisões:
		int c, d ;
		double resultado;
		
			c = 5;
			d = 2;
			resultado = (double) c / d;
			
			System.out.println(resultado);
			
			System.out.println("");
			
		//Expressão matemática envolvendo área:
		double b, B, h, area;
		
			b = 6.0;
			B = 8.0;
			h = 5.0;
			area = (b + B) / 2.0 * h;
			
			System.out.println(area);
		
			System.out.println("");
			
			
	}

}
